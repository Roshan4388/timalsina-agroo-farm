// Translation Manager
const TranslationManager = {
  currentLanguage: localStorage.getItem('language') || 'en',
  
  init() {
    this.applyLanguage(this.currentLanguage);
  },
  
  setLanguage(lang) {
    if (translations[lang]) {
      this.currentLanguage = lang;
      localStorage.setItem('language', lang);
      this.applyLanguage(lang);
      this.updateLanguageSwitcher();
    }
  },
  
  getTranslation(key) {
    return translations[this.currentLanguage][key] || translations['en'][key] || key;
  },
  
  applyLanguage(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
      const key = element.getAttribute('data-translate');
      const translation = this.getTranslation(key);
      
      // Check if element has children and preserve structure
      if (element.innerHTML.includes('<')) {
        element.innerHTML = translation;
      } else {
        element.textContent = translation;
      }
    });
    
    // Update document language attribute
    document.documentElement.lang = lang;
  },
  
  updateLanguageSwitcher() {
    const btns = document.querySelectorAll('[data-lang]');
    btns.forEach(btn => {
      btn.classList.remove('active');
      if (btn.getAttribute('data-lang') === this.currentLanguage) {
        btn.classList.add('active');
      }
    });
  }
};

// Initialize translation when DOM is ready
document.addEventListener('DOMContentLoaded', function() {
  TranslationManager.init();
  
  // Add event listeners to language switcher buttons
  document.querySelectorAll('[data-lang]').forEach(btn => {
    btn.addEventListener('click', function(e) {
      e.preventDefault();
      const lang = this.getAttribute('data-lang');
      TranslationManager.setLanguage(lang);
    });
  });
});
